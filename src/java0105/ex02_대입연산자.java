package java0105;

public class ex02_대입연산자 {
public static void main(String[] args) {
	//연산의 결과를 저장하고 싶은 경우
	// 기호 : =
	//복합대입연산자 : +=, -= *=, /=,%=.......
	
	int a = 10;
	int b = 20;
	int c = 30;
	
	a+=b; // a=a+b;
	b-=c; // b=b-c
	//복합연산자 계산은 제일 마지막이라는거 항상 인지해야됨
	
	
	
	
	
	
}
}
